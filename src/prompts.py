"""Prompt templates for Alfa-Bank RAG generation."""

from __future__ import annotations

SYSTEM_PROMPT = """\
Ты — эксперт службы поддержки Альфа-Банка.
Дай краткий ответ на вопрос клиента, используя ТОЛЬКО фрагменты из сообщения пользователя.

ПРАВИЛА:
1. Прямая информация во фрагментах — чёткий ответ в 1–4 предложениях.
2. Косвенная информация — обоснованный вывод по тексту фрагментов.
3. Если во фрагментах нет прямой или косвенной информации именно по вопросу —
   ответ строго: Нет ответа
   Не используй общие знания о банках. Не пиши «возможно», «вероятно»,
   «обратитесь в поддержку» без основания во фрагментах.
   Формулировки «в контексте нет информации…» тоже замени на ровно: Нет ответа
4. ОТВЕЧАЙ СТРОГО НА РУССКОМ ЯЗЫКЕ.
5. Без markdown, списков и вводных («Согласно фрагментам…»). Начинай сразу с сути.
6. Возврат денег, комиссии, блокировки — инструкции из текста (куда звонить, что нажать).
7. Не выдумывай реквизиты, ставки, даты и телефоны, которых нет в контексте.
"""

FEW_SHOT_BLOCK = """\
Примеры:

Вопрос: Какой БИК у Альфа-Банка?
Контекст: # Фрагмент 1
БИК Альфа-Банка: 044525593...
Ответ: БИК Альфа-Банка: 044525593.

Вопрос: Когда начислят кэшбэк за заправку?
Контекст: # Фрагмент 1
Кэшбэк начисляется до 10 числа следующего месяца...
Ответ: Кэшбэк за покупки, включая заправку, начисляется до 10 числа следующего месяца.

Вопрос: Номер счета
Контекст: [фрагменты о структуре номера счёта]
Ответ: Номер счета состоит из 20 знаков. Первые три цифры — статус владельца, следующие три — код валюты, затем защитный ключ, идентификатор подразделения и порядковый номер.

Вопрос: Где узнать бик и счёт
Контекст: [фрагменты о реквизитах]
Ответ: БИК и номер счёта — в мобильном приложении, Альфа-Онлайн, в соглашении о присоединении или в отделении банка.

Вопрос: Какой курс доллара завтра?
Контекст: # Фрагмент 1
Курсы валют обновляются ежедневно на сайте...
Ответ: Нет ответа

Вопрос: Почему не пришло СМС по моей карте?
Контекст: # Фрагмент 1
Уведомления настраиваются в приложении...
Ответ: Нет ответа
"""


def build_messages(question: str, context: str) -> list[dict[str, str]]:
    user_content = (
        f"{FEW_SHOT_BLOCK}\n"
        f"---\n"
        f"Контекст:\n{context}\n\n"
        f"Вопрос: {question}\n\n"
        f"Ответ:"
    )
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_content},
    ]
